import java.util.Scanner;

public class Main {
	
	public static void Main(String[] args) 
	
	{
		Scanner scan = new Scanner(System.in);
	
		int a = scan.nextInt(); 
		
		switch (a) {
		case 1:
			System.out.println("1");
			
			break;
		case 2:
			System.out.println("2"); 

	
			break;
		}
	}

}
