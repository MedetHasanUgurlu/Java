
public class Car {
	
	
	private String CarName;
	private int id;
	
	
	public Car(String CarName,int id) {
		this.CarName = CarName;
		this.id = id;
				
	}
	
	public String getCarName() {
		
		return CarName;
				
		
		
	}
	
	public void setCarName(String carName) {
		this.CarName = carName;
		
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	
	
	
	
	

}
